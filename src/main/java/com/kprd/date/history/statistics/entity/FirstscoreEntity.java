package com.kprd.date.history.statistics.entity;


public class FirstscoreEntity {
	
	/**球队编号*/
	private Integer tid;
	/**球队名称*/
	private String name;
	/**主场最先入球场数*/
	private String hs;
	/**主场最先失球场数*/
	private String hl;
	/**客场最先入球场数*/
	private String as;
	/**客场最先失球场数*/
	private String al;
	
	
	public Integer getTid() {
		return tid;
	}
	public void setTid(Integer tid) {
		this.tid = tid;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getHs() {
		return hs;
	}
	public void setHs(String hs) {
		this.hs = hs;
	}
	public String getHl() {
		return hl;
	}
	public void setHl(String hl) {
		this.hl = hl;
	}
	public String getAs() {
		return as;
	}
	public void setAs(String as) {
		this.as = as;
	}
	public String getAl() {
		return al;
	}
	public void setAl(String al) {
		this.al = al;
	}
	
	
	
}
