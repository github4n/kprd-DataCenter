package com.kprd.date.history.statistics.entity;


public class PlayerEventEntity {
	
	/**主事件状态*/
	private String la_img;
	/**主队信息*/
	private String la;
	/**事件时间*/
	private String tm;	
	/**客队信息*/
	private String lb;
	/**客事件状态*/
	private String lb_img;

	public String getLa_img() {
		return la_img;
	}

	public void setLa_img(String la_img) {
		this.la_img = la_img;
	}

	public String getLa() {
		return la;
	}

	public void setLa(String la) {
		this.la = la;
	}

	public String getTm() {
		return tm;
	}

	public void setTm(String tm) {
		this.tm = tm;
	}

	public String getLb() {
		return lb;
	}

	public void setLb(String lb) {
		this.lb = lb;
	}

	public String getLb_img() {
		return lb_img;
	}

	public void setLb_img(String lb_img) {
		this.lb_img = lb_img;
	}
	
	
	
}
