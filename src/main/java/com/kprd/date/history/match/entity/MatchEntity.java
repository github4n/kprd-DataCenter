package com.kprd.date.history.match.entity;

public class MatchEntity {
	/**联赛编号*/
	private Integer lid;
	/**联赛名*/
	private String cn;
	/**联赛名（英文）*/
	private String en;
	/**联赛名（繁体）*/
	private String gbk;
	/**联赛名(简称)*/
	private String cnshort;

	private String gbkshort;
	/**所属区域编号*/
	private String color;
	/**联赛所属国家编号*/
	private Integer cid;
	/**联赛类型*/
	private Integer kind;
	
	
	public String getCnshort() {
		return cnshort;
	}
	public void setCnshort(String cnshort) {
		this.cnshort = cnshort;
	}
	public String getGbkshort() {
		return gbkshort;
	}
	public void setGbkshort(String gbkshort) {
		this.gbkshort = gbkshort;
	}
	public Integer getLid() {
		return lid;
	}
	public void setLid(Integer lid) {
		this.lid = lid;
	}
	public String getCn() {
		return cn;
	}
	public void setCn(String cn) {
		this.cn = cn;
	}
	public String getEn() {
		return en;
	}
	public void setEn(String en) {
		this.en = en;
	}
	public String getGbk() {
		return gbk;
	}
	public void setGbk(String gbk) {
		this.gbk = gbk;
	}
	
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	public Integer getCid() {
		return cid;
	}
	public void setCid(Integer cid) {
		this.cid = cid;
	}
	public Integer getKind() {
		return kind;
	}
	public void setKind(Integer kind) {
		this.kind = kind;
	}
	
	
}
